export function exportAs(
  format: ExportFormat,
  payload: ExportPayload,
): { content: string; mime: string; filename: string } {
  const slug =
    payload.video.title
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "")
      .slice(0, 50) || "report";
  switch (format) {
    case "html":
      return { content: toHtml(payload), mime: "text/html", filename: `${slug}.html` };
    case "markdown":
      return { content: toMarkdown(payload), mime: "text/markdown", filename: `${slug}.md` };
    case "txt":
      return { content: toTxt(payload), mime: "text/plain", filename: `${slug}.txt` };
    case "json":
      return { content: toJson(payload), mime: "application/json", filename: `${slug}.json` };
    case "csv":
      return { content: toCsv(payload), mime: "text/csv", filename: `${slug}-comments.csv` };
    case "sheets":
      return {
        content: `${SHEETS_HEADER}\n${toSheetsRow(payload)}`,
        mime: "text/tab-separated-values",
        filename: `${slug}.tsv`,
      };
  }
}
