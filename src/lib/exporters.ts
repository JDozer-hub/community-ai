import type {
  FullReport,
  StoredComment,
  ThemeGroup,
  VideoMeta,
} from "./schema";

export type ExportPayload = {
  video: VideoMeta;
  report: FullReport;
  comments: StoredComment[];
};

export type ExportFormat = "html" | "markdown" | "txt" | "json" | "csv" | "sheets";

function pct(n: number): string {
  return `${Math.round(n)}%`;
}

function shortDate(iso: string): string {
  const d = new Date(iso);
  return Number.isNaN(d.getTime()) ? "" : d.toISOString().slice(0, 10);
}

function evidenceLines(comments: StoredComment[], max = 4): string[] {
  return comments.slice(0, max).map((c) => {
    const meta = `👍 ${c.likeCount}${shortDate(c.publishedAt) ? ` · ${shortDate(c.publishedAt)}` : ""}`;
    return `    - ${c.author} (${meta})\n      "${c.text.replace(/\s+/g, " ").trim()}"`;
  });
}

function themeLines(items: ThemeGroup[]): string[] {
  if (!items.length) return ["- (none detected)"];
  const out: string[] = [];
  for (const t of items) {
    out.push(`- ${t.theme} — ${t.mentionCount} mention${t.mentionCount === 1 ? "" : "s"}`);
    out.push(...evidenceLines(t.comments));
  }
  return out;
}

