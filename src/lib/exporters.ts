export function toMarkdown({ video, report }: ExportPayload): string {
  const lines: string[] = [];
  lines.push(`# Community AI Report — ${video.title}`);
  lines.push("");
  lines.push(`**Channel:** ${video.channelTitle}  `);
  lines.push(`**Video:** ${video.url}  `);
  lines.push(`**Published:** ${video.publishedAt}  `);
  lines.push(
    `**Views:** ${video.viewCount.toLocaleString()} · **Likes:** ${video.likeCount.toLocaleString()} · **Comments analyzed:** ${report.totalComments.toLocaleString()} (${report.totalReplies.toLocaleString()} replies)  `,
  );
  lines.push(
    `**Sentiment:** ${report.sentiment.label} — ${pct(report.sentiment.positivePct)} positive / ${pct(report.sentiment.neutralPct)} neutral / ${pct(report.sentiment.negativePct)} negative  `,
  );
  lines.push(`**Confidence:** ${Math.round(report.confidence * 100)}%`);
  lines.push("");
  lines.push(`## Summary`);
  lines.push(report.summary);
  lines.push("");

  if (report.topComment) {
    lines.push(`## Top Comment`);
    lines.push(
      `> ${report.topComment.text}\n> — ${report.topComment.author} · 👍 ${report.topComment.likeCount.toLocaleString()} · ${shortDate(report.topComment.publishedAt)}`,
    );
    lines.push("");
  }

  lines.push(`## Most Requested Future Episodes`);
  if (report.mostRequestedFutureEpisodes.length) {
    for (const e of report.mostRequestedFutureEpisodes) {
      lines.push(`### ${e.title} — ${e.mentionCount} mentions`);
      if (e.supporters.length) {
        lines.push(
          `- Top supporters: ${e.supporters
            .map((s) => `${s.author} (${s.commentCount})`)
            .join(", ")}`,
        );
      }
      lines.push(`- Representative comments:`);
      lines.push(...evidenceLines(e.comments));
    }
  } else {
    lines.push("- (none detected)");
  }
  lines.push("");

  for (const s of SECTIONS) {
    lines.push(`## ${s.label}`);
    lines.push(...themeLines(report[s.key] as ThemeGroup[]));
    lines.push("");
  }

  lines.push(`## Moderation Issues`);
  if (report.moderationIssues.length) {
    for (const m of report.moderationIssues) {
      lines.push(`- **${m.type}** — ${m.count}`);
      lines.push(...evidenceLines(m.comments));
    }
  } else {
    lines.push("- (none detected)");
  }
  lines.push("");

  lines.push(`## Action Items`);
  if (report.actionItems.length) {
    report.actionItems.forEach((a, i) => lines.push(`${i + 1}. ${a}`));
  } else {
    lines.push("- (none)");
  }
  lines.push("");
  lines.push(`---`);
  lines.push(`_Generated by Community AI._`);
  return lines.join("\n");
}

export function toTxt(payload: ExportPayload): string {
  return toMarkdown(payload)
    .replace(/^#+\s*/gm, "")
    .replace(/\*\*/g, "")
    .replace(/^> ?/gm, "")
    .replace(/`/g, "");
}

export function toJson(payload: ExportPayload): string {
  return JSON.stringify(payload, null, 2);
}

function csvEscape(value: string | number | boolean | null): string {
  const s = value === null || value === undefined ? "" : String(value);
  if (/[",\n]/.test(s)) return `"${s.replace(/"/g, '""')}"`;
  return s;
}

export function toCsv({ comments }: ExportPayload): string {
  const header = [
    "comment_id",
    "parent_id",
    "author",
    "author_channel_id",
    "is_reply",
    "like_count",
    "published_at",
    "updated_at",
    "text_original",
  ];
  const rows = comments.map((c) =>
    [
      c.commentId,
      c.parentId ?? "",
      c.author,
      c.authorChannelId ?? "",
      c.isReply,
      c.likeCount,
      c.publishedAt,
      c.updatedAt,
      c.text.replace(/\s+/g, " ").trim(),
    ]
      .map(csvEscape)
      .join(","),
  );
  return [header.join(","), ...rows].join("\n");
}

function topTheme(items: ThemeGroup[]): string {
  if (!items.length) return "";
  const t = items[0];
  return `${t.theme} (${t.mentionCount})`;
}

/** Single tab-separated row for pasting into an existing Google Sheets tracker. */
export function toSheetsRow({ video, report }: ExportPayload): string {
  const cells = [
    new Date().toISOString().slice(0, 10),
    video.channelTitle,
    video.title,
    video.url,
    report.totalComments,
    report.sentiment.label,
    `${pct(report.sentiment.positivePct)}/${pct(report.sentiment.neutralPct)}/${pct(report.sentiment.negativePct)}`,
    `${Math.round(report.confidence * 100)}%`,
    report.mostRequestedFutureEpisodes
      .slice(0, 3)
      .map((e) => `${e.title} (${e.mentionCount})`)
      .join("; "),
    topTheme(report.criticism),
    topTheme(report.sponsorFeedback),
    topTheme(report.hostFeedback),
    topTheme(report.editingFeedback),
    report.moderationIssues.map((m) => `${m.type} (${m.count})`).join("; "),
    report.actionItems.slice(0, 5).join(" | "),
    report.summary.replace(/\s+/g, " ").trim(),
  ];
  return cells.map((c) => String(c).replace(/[\t\n\r]+/g, " ").trim()).join("\t");
}

